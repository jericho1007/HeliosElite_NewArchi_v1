void PUBLIC_SwapU32Data(uint8_t *srcData)
{
	uint8_t temp;

	temp = srcData[0];
	srcData[0] = srcData[3];
	srcData[3] = temp;

	temp = srcData[1];
	srcData[1] = srcData[2];
	srcData[2] = temp;
}
